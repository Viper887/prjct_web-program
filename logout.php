<?php
// 1. Запускаємо сесію, щоб отримати доступ до поточної сесії користувача
session_start();

// 2. Очищаємо всі змінні сесії
$_SESSION = [];

// 3. Знищуємо сесію на сервері
session_destroy();

// 4. Перенаправляємо користувача на головну сторінку
header("Location: index.php");
exit(); // Важливо викликати exit після header, щоб зупинити виконання скрипта
?>